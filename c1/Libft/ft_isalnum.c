int	ft_isalnum(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	if (c >= 0060 && c <= 0071)
		return (1);
	if (c >= 0x30 && c <= 0x39)
		return (1);
	if ((c >= 101 && c <= 132) || (c >= 141 && c <= 172))
		return (1);
	if ((c >= 0101 && c <= 0132) || (c >= 0141 && c <= 0172))
		return (1);
	if ((c >= 0x41 && c <= 0x5a) || (c >= 0x61 && c <= 0x7a))
		return (1);
	return (0);
}
