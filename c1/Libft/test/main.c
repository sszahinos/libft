#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include "../libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == c)
		return ((char *)s + i);
	return (0);
}

int	main(void)
{
	printf("%p", ft_strchr("teste",'e'));
	return (0);
}
